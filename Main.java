class Solution {
    public int minSteps(int n) {
        if (n == 1) return 0;  // Если n = 1, то никаких операций не требуется, возвращаем 0.

        int steps = 0;  // Переменная для хранения минимального количества операций.
        int factor = 2;  // Начинаем с делителя 2.

        // Мы будем делить n на его простые делители, пока n > 1.
        while (n > 1) {
            // Если текущий делитель делит n без остатка, это означает, что мы можем использовать операцию копирования и вставки.
            while (n % factor == 0) {
                steps += factor;  // Увеличиваем количество операций на величину текущего делителя.
                n /= factor;  // Делим n на текущий делитель.
            }
            factor++;  // Переходим к следующему возможному делителю.
        }

        return steps;  // Возвращаем общее количество операций.
    }
}